#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';

/**
 * Fill your API keys in CONFIG, then run: npm run setup:api
 */
const CONFIG = {
  provider: 'mock', // deepseek | gemini | mock

  deepseek: {
    apiKey: 'your api key',
    model: 'deepseek-chat',
    endpoint: 'https://api.deepseek.com/chat/completions',
  },

  gemini: {
    apiKey: 'your api key',
    model: 'gemini-2.5-flash',
    endpoint: 'https://generativelanguage.googleapis.com/v1/models',
    imageModel: 'gemini-2.5-flash-image',
    imageEndpoint: 'https://generativelanguage.googleapis.com/v1beta/models',
  },
};

const rootDir = process.cwd();
const envPath = path.join(rootDir, '.env.local');

const normalizedProvider = String(CONFIG.provider || 'mock').trim().toLowerCase();
const validProvider = ['deepseek', 'gemini', 'mock'].includes(normalizedProvider)
  ? normalizedProvider
  : 'mock';

const lines = [
  '# Auto-generated by scripts/setup-api-keys.mjs',
  `VITE_AI_PROVIDER=${validProvider}`,
  `VITE_DEEPSEEK_API_KEY=${String(CONFIG.deepseek?.apiKey || '').trim()}`,
  `VITE_DEEPSEEK_MODEL=${String(CONFIG.deepseek?.model || 'deepseek-chat').trim()}`,
  `VITE_DEEPSEEK_ENDPOINT=${String(CONFIG.deepseek?.endpoint || 'https://api.deepseek.com/chat/completions').trim()}`,
  `VITE_GEMINI_API_KEY=${String(CONFIG.gemini?.apiKey || '').trim()}`,
  `VITE_GEMINI_MODEL=${String(CONFIG.gemini?.model || 'gemini-2.5-flash').trim()}`,
  `VITE_GEMINI_ENDPOINT=${String(CONFIG.gemini?.endpoint || 'https://generativelanguage.googleapis.com/v1/models').trim()}`,
  `VITE_GEMINI_IMAGE_MODEL=${String(CONFIG.gemini?.imageModel || 'gemini-2.5-flash-image').trim()}`,
  `VITE_GEMINI_IMAGE_ENDPOINT=${String(CONFIG.gemini?.imageEndpoint || 'https://generativelanguage.googleapis.com/v1beta/models').trim()}`,
  '',
];

fs.writeFileSync(envPath, lines.join('\n'), 'utf8');

console.log('Generated .env.local');
console.log(`Path: ${envPath}`);
console.log(`Provider: ${validProvider}`);
if (validProvider !== 'mock') {
  console.log('Update CONFIG and run npm run setup:api again to overwrite values.');
}
